#!/bin/bash
# Вказуємо, що скрипт виконується за допомогою Bash-інтерпретатора

target=$((RANDOM % 100 + 1))
# Зберігаємо випадкове число від 1 до 100 у змінну "target"

attempts=5
# Задаємо максимальну кількість спроб

for ((i=1; i<=attempts; i++)); do
    # Цикл повторюється 5 разів (від 1 до 5)
    
    read -p "Спроба $i з $attempts. Вгадайте число від 1 до 100: " guess
    # Виводимо запрошення і зчитуємо відповідь користувача в змінну "guess"
    
    if (( guess == target )); then
        echo "Вітаємо! Ви вгадали правильне число."
        exit 0
        # Якщо відповідь правильна — виводимо повідомлення і завершуємо роботу скрипта
    elif (( guess > target )); then
        echo "Занадто високо."
        # Якщо число більше за правильне
    else
        echo "Занадто низько."
        # Якщо число менше за правильне
    fi
done

echo "Вибачте, у вас закінчилися спроби. Правильним числом було $target."
# Якщо користувач не вгадав — показуємо правильну відповідь

